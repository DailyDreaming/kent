#!/bin/sh

echo "================================================================
See also: the file in this directory: bigBedWigs.txt
	for information on operating the bigWig and bigBed
	utilities and loading custom tracks
================================================================
" > README.txt

echo "================================================================" \
	>> README.txt

find . -mindepth 1 -type f | sed -e "s/^.\///; /mkREADME.sh/d" | sort | \
while read F
do
    if [ -x "${F}" ]; then
	echo "========   ${F}   ===================================="
	echo "================================================================"
	./${F}
	echo "================================================================"
	fi
done >> README.txt 2>&1

